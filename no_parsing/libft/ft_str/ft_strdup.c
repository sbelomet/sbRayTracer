#include "../includes/libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*str;

	str = (char *) malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = (char)s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}

char	*ft_strdup_allocated(const char *s1, t_alloc **alloc)
{
	char	*s2;
	int		i;

	i = ft_strlen(s1);
	s2 = (char *)ft_malloc((i * sizeof(char) + 1), alloc);
	if (!s2)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
